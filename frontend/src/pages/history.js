export function buildHistorySpec(state) {
  const heading = el("h1", { className: "history-page__title", text: "Historical Analytics" });

  if (state.status === "loading") {
    return el("div", { className: "history-page", children: [heading, loadingSectionSpec()] });
  }

  if (state.status === "error") {
    return el("div", {
      className: "history-page",
      children: [heading, errorBannerSpec({ message: describeHistoryFetchError(state.error), icon: "error" })],
    });
  }

  const banners = [];
  if (state.error) {
    banners.push(errorBannerSpec({ message: describeHistoryFetchError(state.error), icon: "error" }));
  } else if (state.isStale) {
    const latest = state.data.snapshots[state.data.snapshots.length - 1];
    banners.push(errorBannerSpec({ message: staleMessage(latest && latest.timestamp), icon: "warning" }));
  }

  // Section 5's diagram lists this page's components tersely as
  // "ChartPanel x N" with no explicit EmptyState -- but Section 16's
  // three-state requirement (Loading/Error/Empty) is a blanket
  // architectural rule applied to every view, not opted into per page
  // by the diagram; every other list-style page (Pool/Users/Workers/
  // Ticker) already treats it that way, so this page does too.
  if (state.status === "empty") {
    return el("div", {
      className: "history-page",
      children: [
        heading,
        ...banners,
        cardSpec({ children: [emptyStateSpec({ message: "No historical data has been recorded yet." })] }),
      ],
    });
  }

  if (state.status !== "success") {
    throw new Error(`buildHistorySpec: unrecognized status "${state.status}"`);
  }

  const charts = CHART_DEFS.map((def) =>
    chartPanelSpec({
      title: def.title,
      summary: def.buildSummary(state.data.snapshots),
    }),
  );

  return el("div", {
    className: "history-page",
    children: [heading, ...banners, ...charts],
  });
}

