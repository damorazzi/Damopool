// -------------------------------------------------------------------
// Pure parsing/formatting for this legacy, string-encoded data source
// -------------------------------------------------------------------

const SIZE_UNIT_FACTORS = [
  { suffix: "P", factor: 1e15 },
  { suffix: "T", factor: 1e12 },
  { suffix: "G", factor: 1e9 },
  { suffix: "M", factor: 1e6 },
  { suffix: "K", factor: 1e3 },
];

// Accepts either the legacy suffixed string ("14.2T") or an already-
// numeric value (defensive: nothing in this project's own docs
// guarantees this pre-existing file's field types, only the sample
// data observed) -- either way returns a plain number of base units,
// or null for anything that parses as neither.
export function parseSizeString(value) {
  if (typeof value === "number") {
    return Number.isFinite(value) ? value : null;
  }
  if (typeof value !== "string") {
    return null;
  }
  const match = value.trim().match(/^(-?\d+(?:\.\d+)?)\s*([KMGTP]?)$/i);
  if (!match) return null;
  const [, numberPart, unitPart] = match;
  const unit = SIZE_UNIT_FACTORS.find((u) => u.suffix === unitPart.toUpperCase());
  return parseFloat(numberPart) * (unit ? unit.factor : 1);
}

function scaleBySizeUnit(value) {
  const abs = Math.abs(value);
  const unit = SIZE_UNIT_FACTORS.find((u) => abs >= u.factor);
  return unit ? { scaled: value / unit.factor, suffix: unit.suffix } : { scaled: value, suffix: "" };
}

export function formatHashrate(value) {
  if (!Number.isFinite(value)) return null;
  const { scaled, suffix } = scaleBySizeUnit(value);
  return `${scaled.toFixed(2)} ${suffix}H/s`;
}

// A compact axis-tick-friendly form of a difficulty value -- distinct
// from formatSdiff (core/format.js), which every other page in this
// project already uses for a *full*, comma-separated difficulty number
// in prose/table text, where space isn't constrained the way a chart
// Y-axis tick label's is. Both are used below: this one for the chart
// itself, formatSdiff for the accessible summary text, matching the
// established project-wide difficulty display convention there.
export function formatCompactDifficulty(value) {
  if (!Number.isFinite(value)) return null;
  const { scaled, suffix } = scaleBySizeUnit(value);
  return suffix ? `${scaled.toFixed(2)} ${suffix}` : scaled.toFixed(2);
}

function formatCount(n) {
  return Number.isFinite(n) ? n.toLocaleString("en-US") : null;
}

