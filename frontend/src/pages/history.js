export function buildSharesChartSummary(snapshots) {
  const accepted = snapshots.map((s) => s.acceptedShares).filter((v) => Number.isFinite(v));
  const rejected = snapshots.map((s) => s.rejectedShares).filter((v) => Number.isFinite(v));
  if (accepted.length === 0 && rejected.length === 0) return "Accepted and rejected shares -- no data available.";
  const latestAccepted = accepted.length > 0 ? formatCount(accepted[accepted.length - 1]) : "no data";
  const latestRejected = rejected.length > 0 ? formatCount(rejected[rejected.length - 1]) : "no data";
  return `Cumulative accepted and rejected shares over ${snapshots.length} snapshots -- most recently ${latestAccepted} accepted, ${latestRejected} rejected.`;
}

