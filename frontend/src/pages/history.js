// Historical Analytics page -- docs/ARCHITECTURE.md Section 4/5/9
// (`/app/#/history`). Composes ChartPanel x N per Section 5's
// component list -- no StatTile, no DataTable, no SearchBox.
// shell.js's APP_NAV_ITEMS already had a History nav entry since
// Milestone 5, so no shell.js change is needed here.
//
// The first page in this project that does NOT read analytics.json.
// docs/ARCHITECTURE.md Section 25 explicitly defers multi-day history
// to a separate, not-yet-built analytics_history.json
// (PROJECT_LOG.md 2026-07-15); analytics.json's own daily_bests holds
// only today and yesterday. What already exists, today, in production
// is the *existing*, CLAUDE.md-protected `historical_data.json` --
// generated by the pre-existing parse_pool_stats.py (CLAUDE.md: "Do
// not modify the existing parser... Do not modify historical_data.json
// structure"), already served at a real, already-existing Nginx
// location (`/historical_data`, confirmed by reading
// /etc/nginx/sites-available/default -- no new Nginx work needed here,
// unlike analytics.json's own still-pending Section 15 cutover item).
// This page only ever reads that file; it never writes to it, and
// nothing here depends on or assumes any future analytics_history.json
// contract.
//
// Because this is a different file with a different, pre-existing
// shape (a bare JSON array of `{timestamp, pool_stats: {...}}`
// snapshots, not an object with `metadata`/`pool`/`users`/etc.), this
// page deliberately does NOT reuse several analytics.json-specific
// primitives that would silently misbehave against this shape:
//   - core/errors.js's `validateSchema` checks for analytics.json's own
//     top-level keys and `metadata.schema_version` -- neither exists
//     on this array, so it would reject every real response. A small,
//     page-local `isValidHistoricalData` checks the one thing that
//     actually matters here (the payload is an array).
//   - core/errors.js's `describeFetchError` says "analytics
//     service"/"analytics data," which would be a factually wrong
//     message for a `/historical_data` failure. A page-local
//     `describeHistoryFetchError` mirrors its exact kind-based
//     branching with corrected wording -- not a duplicate of its
//     logic in spirit, just correctly scoped wording for a different
//     data source, kept local rather than added to a shared module for
//     its only consumer (the same "extract once a second consumer
//     needs it" discipline already established for describeFetchError
//     itself).
//   - core/api.js's built-in staleness detection reads
//     `payload.metadata.generated_at`, which this array does not have
//     -- passing `staleAfterMs` straight through to fetchEndpoint would
//     make every response report `isStale: true` unconditionally, a
//     wrong result, not a missing one. Staleness here is instead
//     computed locally, in deriveHistoryState, from the *data's own*
//     freshness signal: the most recent snapshot's own `timestamp`.
//   - core/state.js's `analytics`/`analyticsFetchedAt` fields are
//     documented as holding "the last-fetched analytics.json payload"
//     specifically, shared because multiple pages consume it. Nothing
//     else in this project reads historical_data.json, so there is no
//     sharing need, and writing this array into that slot would
//     corrupt its documented, single-purpose meaning for the one
//     narrow window before this page's next fetch overwrites it. This
//     page's fetched payload lives only in its own module closure.
//
// pool_stats' own numeric fields arrive as legacy, human-formatted
// strings ("14.2T", "241.08G"), not raw numbers -- parseSizeString
// below is the first "parse a formatted string back into a number"
// utility in this project (every existing core/format.js helper only
// goes the other direction). Kept page-local rather than added to
// core/format.js for the same single-consumer reasoning as above.
//
// No prototype-chain risk and no free-text/XSS surface exists on this
// page at all: historical_data.json is an array of snapshots (never a
// dictionary keyed by username/workername), and this first version
// deliberately shows pool-level aggregates only -- no per-user
// breakdown, so no attacker-influenceable string (a username, an
// agent string) is read or rendered anywhere in this file.
//
// The four charts below are the first genuine time-series charts in
// this project -- every prior chart (Overview/Pool/User Detail/Worker
// Detail) plots a small, fixed category axis (15m/1h/24h, p50/p90/p99),
// never real timestamps. ECharts' own `xAxis: {type: "time"}` is used
// here instead of hand-rolling date-bucketing/label logic -- it is
// built for exactly this and correctly adapts its own tick labels to
// however wide a range the data actually spans, which a fixed
// `formatTimestamp` (core/format.js's HH:MM-only formatter, sufficient
// for every prior chart's same-day rolling windows) cannot do once the
// range can span multiple days.
//
// Also the first page with more than one chart -- defaultRender below
// generalises the single-canvas-swap technique every prior chart page
// uses to an *array* of canvas nodes, reused/created/disposed
// positionally (the charts are always rendered in the same fixed
// order, so index-based matching is sufficient and needs no per-chart
// identifier).

