const EMPTY_PAGE_SPEC = el("div", { className: "history-page" });

// Generalises overview.js's/pool.js's/*-detail.js's single-canvas swap
// to an array of canvas nodes, one per CHART_DEFS entry, matched
// positionally (document order === CHART_DEFS order, always, since the
// spec above always renders them in that fixed sequence).
function defaultRender(container, spec, { reuseCanvasNodes = [] } = {}) {
  const node = specToDom(spec);
  const freshCanvasNodes = Array.from(node.querySelectorAll(".chart-panel__canvas"));

  const canvasNodes = freshCanvasNodes.map((freshNode, index) => {
    const reuseNode = reuseCanvasNodes[index];
    if (reuseNode && freshNode && freshNode.parentNode) {
      freshNode.parentNode.replaceChild(reuseNode, freshNode);
      return reuseNode;
    }
    return freshNode;
  });

  container.replaceChildren(node);
  return canvasNodes;
}

function emptyChartSlots() {
  return CHART_DEFS.map(() => null);
}

// Injectable (tests can capture calls without polluting test output);
// defaults to console.error rather than silently discarding, since
// this project has no other error-reporting/telemetry mechanism to
// hook into and "reported nowhere" was the specific gap this callback
// exists to close.
function defaultOnChartError(error, context) {
  if (typeof console !== "undefined" && console.error) {
    console.error(`history.js: ${context}`, error);
  }
}

let isMounted = false;
let mountToken = 0;
let chartHandles = emptyChartSlots();
let chartCanvasNodes = emptyChartSlots();
let renderedStatus = null;
let lastSnapshots = null;
let lastAppliedTheme = null;
let stopPollingFn = null;
let stopThemeSubscription = null;
let currentRender = defaultRender;
let currentContainer = null;

function disposeAllCharts() {
  chartHandles.forEach((handle) => {
    if (handle) handle.dispose();
  });
  chartHandles = emptyChartSlots();
  chartCanvasNodes = emptyChartSlots();
}

export function mount(
  container,
  {
    fetchImpl,
    intervalMs,
    staleAfterMs,
    render = defaultRender,
    createChartImpl = createChart,
    readThemeTokensImpl = readThemeTokens,
    onChartError = defaultOnChartError,
  } = {},
) {
  if (isMounted) {
    throw new Error("history.mount: already mounted -- call unmount() first");
  }
  isMounted = true;
  mountToken += 1;
  const myToken = mountToken;

  chartHandles = emptyChartSlots();
  chartCanvasNodes = emptyChartSlots();
  renderedStatus = null;
  lastSnapshots = null;
  stopPollingFn = null;
  stopThemeSubscription = null;
  currentRender = render;
  currentContainer = container;
  lastAppliedTheme = getState().theme;

  function isCurrent() {
    return isMounted && mountToken === myToken;
  }

  const safeStaleAfterMs = Number.isFinite(staleAfterMs) && staleAfterMs > 0 ? staleAfterMs : undefined;
  // No `validate: validateSchema` -- see the module comment. This
  // endpoint's own shape check is isValidHistoricalData. staleAfterMs
  // is deliberately not forwarded to fetchEndpoint either (see the
  // module comment) -- it is applied locally in deriveHistoryState
  // instead, from handleResult below.
  const fetchOptions = { fetchImpl, validate: isValidHistoricalData };

  function renderState(historyState) {
    const reuseCharts = renderedStatus === "success" && historyState.status === "success";

    if (!reuseCharts) {
      disposeAllCharts();
    }

    const spec = buildHistorySpec(historyState);
    const canvasNodes = render(container, spec, {
      reuseCanvasNodes: reuseCharts ? chartCanvasNodes : [],
    });
    renderedStatus = historyState.status;

    if (historyState.status === "success" && Array.isArray(canvasNodes) && canvasNodes.length === CHART_DEFS.length) {
      lastSnapshots = historyState.data.snapshots;
      const theme = buildEChartsTheme(readThemeTokensImpl());
      CHART_DEFS.forEach((def, index) => {
        // Each of the four charts is independent -- one chart's
        // failure (a malformed option, an ECharts init error, e.g.
        // window.echarts not yet loaded) must not prevent every
        // *later* CHART_DEFS entry in this same forEach from being
        // created/updated. Left null (not left holding a possibly-
        // half-initialized handle) so the next render can still retry
        // creating this one fresh, rather than being permanently
        // wedged on a chart that never successfully initialized.
        try {
          const option = def.buildOption(lastSnapshots, theme);
          if (reuseCharts && chartHandles[index]) {
            chartHandles[index].update(option);
          } else {
            chartHandles[index] = createChartImpl(canvasNodes[index], option);
          }
          chartCanvasNodes[index] = canvasNodes[index];
        } catch (error) {
          chartHandles[index] = null;
          chartCanvasNodes[index] = null;
          onChartError(error, def.key);
        }
      });
    } else {
      chartCanvasNodes = emptyChartSlots();
    }
  }

  function handleResult(result) {
    try {
      renderState(deriveHistoryState({ payload: result.payload, error: result.error, staleAfterMs: safeStaleAfterMs }));
    } catch (error) {
      // Defence in depth: the per-chart try/catch above already
      // isolates the one currently-reachable throw site inside
      // renderState, but an unguarded exception anywhere else on this
      // path would otherwise become an unhandled promise rejection on
      // the very first fetch -- leaving the page stuck on its loading
      // skeleton forever with no visible indication anything went
      // wrong -- or, on a later poll tick, get silently discarded by
      // startPolling's own no-onError-supplied default (core/api.js).
      onChartError(error, "renderState");
    }
  }

  renderState({ status: "loading" });

  stopThemeSubscription = subscribe((appState) => {
    if (!isCurrent()) return;
    if (appState.theme === lastAppliedTheme) return;
    lastAppliedTheme = appState.theme;
    if (renderedStatus === "success" && lastSnapshots) {
      const theme = buildEChartsTheme(readThemeTokensImpl());
      CHART_DEFS.forEach((def, index) => {
        if (!chartHandles[index]) return;
        try {
          chartHandles[index].update(def.buildOption(lastSnapshots, theme));
        } catch (error) {
          onChartError(error, `${def.key}-theme-repaint`);
        }
      });
    }
  });

  (async () => {
    const result = await fetchEndpoint(HISTORY_ENDPOINT, fetchOptions);
    if (!isCurrent()) return;
    handleResult(result);

    if (Number.isFinite(intervalMs) && intervalMs > 0) {
      stopPollingFn = startPolling(
        HISTORY_ENDPOINT,
        intervalMs,
        (pollResult) => {
          if (!isCurrent()) return;
          handleResult(pollResult);
        },
        fetchOptions,
      );
    }
  })();
}

export function unmount() {
  if (!isMounted) return;
  isMounted = false;
  if (stopThemeSubscription) {
    stopThemeSubscription();
    stopThemeSubscription = null;
  }
  if (stopPollingFn) {
    stopPollingFn();
    stopPollingFn = null;
  }
  disposeAllCharts();
  renderedStatus = null;
  lastSnapshots = null;
  currentRender(currentContainer, EMPTY_PAGE_SPEC);
  currentContainer = null;
}
