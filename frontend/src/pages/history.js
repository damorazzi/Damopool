function toTimeSeries(snapshots, valueFn) {
  return snapshots
    .map((snapshot) => {
      const value = valueFn(snapshot);
      return value === null || value === undefined ? null : [new Date(snapshot.timestamp).getTime(), value];
    })
    .filter((point) => point !== null);
}

function baseLineChartOption(theme, { yAxisFormatter, legendNames } = {}) {
  return {
    backgroundColor: theme.backgroundColor || "transparent",
    textStyle: theme.textStyle || {},
    legend: legendNames ? { data: legendNames, textStyle: theme.textStyle || {}, top: 0 } : undefined,
    grid: { left: 72, right: 16, top: legendNames ? 40 : 24, bottom: 40, containLabel: true },
    xAxis: {
      type: "time",
      axisLine: theme.axisLine || {},
      axisLabel: theme.axisLabel || {},
    },
    yAxis: {
      type: "value",
      axisLine: theme.axisLine || {},
      axisLabel: yAxisFormatter ? { ...(theme.axisLabel || {}), formatter: yAxisFormatter } : theme.axisLabel || {},
      splitLine: theme.splitLine || {},
    },
  };
}

export function buildHashrateChartOption(snapshots, theme = {}) {
  return {
    ...baseLineChartOption(theme, { yAxisFormatter: (value) => formatHashrate(value) || value }),
    series: [
      {
        name: "Pool Hashrate",
        type: "line",
        showSymbol: false,
        data: toTimeSeries(snapshots, (s) => s.hashrate),
        itemStyle: { color: theme.accentColor || undefined },
        lineStyle: { color: theme.accentColor || undefined },
      },
    ],
  };
}

export function buildHashrateChartSummary(snapshots) {
  const values = snapshots.map((s) => s.hashrate).filter((v) => Number.isFinite(v));
  if (values.length === 0) return "Pool hashrate -- no data available.";
  const latest = formatHashrate(values[values.length - 1]);
  return `Pool hashrate over ${values.length} snapshots -- most recently ${latest || "no data"}.`;
}

