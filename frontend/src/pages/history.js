// A malformed entry (no pool_stats, no timestamp) is dropped rather
// than producing a broken chart point -- the same "degrade gracefully"
// discipline applied everywhere else in this project, here at the
// whole-snapshot level. Defensively sorted ascending by timestamp: the
// live_ticker precedent (search.js/pages/ticker.js) trusts its own
// documented "sorted newest first" contract because that contract is
// this project's own, but historical_data.json's ordering is not
// documented anywhere in CLAUDE.md or the architecture -- it is a
// pre-existing file this project does not own the contract for, so a
// defensive sort costs little and guarantees a correctly-ordered
// x-axis regardless of what the legacy parser's append order actually
// is.
function isParseableTimestamp(value) {
  return typeof value === "string" && !Number.isNaN(new Date(value).getTime());
}

export function transformHistoryData(payload) {
  const entries = Array.isArray(payload) ? payload : [];
  const snapshots = entries
    // A truthy-but-unparseable timestamp (a corrupted string) must be
    // dropped here, alongside a genuinely-missing one -- otherwise
    // new Date(...).getTime() downstream (the sort comparator below,
    // and toTimeSeries' own x-axis positions) silently yields NaN,
    // producing an unpredictable sort order and a real, garbage point
    // plotted on every chart rather than the entry being excluded the
    // way a missing timestamp already is.
    .filter((entry) => entry && isParseableTimestamp(entry.timestamp) && entry.pool_stats)
    .map((entry) => {
      const stats = entry.pool_stats;
      return {
        timestamp: entry.timestamp,
        hashrate: parseSizeString(stats.pool_hashrate),
        acceptedShares: Number.isFinite(stats.accepted_shares) ? stats.accepted_shares : null,
        rejectedShares: Number.isFinite(stats.rejected_shares) ? stats.rejected_shares : null,
        highestDifficulty: parseSizeString(stats.highest_difficulty),
        workers: Number.isFinite(stats.workers) ? stats.workers : null,
        users: Number.isFinite(stats.users) ? stats.users : null,
      };
    })
    .sort((a, b) => new Date(a.timestamp).getTime() - new Date(b.timestamp).getTime());

  return { snapshots };
}

// The one structural check that actually matters for this shape --
// unlike core/errors.js's validateSchema (analytics.json's own
// metadata/schema_version keys, inapplicable here), this file is just
// a bare JSON array; anything else (an object, a string, a parse
// failure already caught upstream) is a genuine schema mismatch, not
// "empty."
export function isValidHistoricalData(payload) {
  if (!Array.isArray(payload)) {
    return { valid: false, reason: "not-an-array" };
  }
  return { valid: true, reason: null };
}

function describeHistoryFetchError(error) {
  if (!error) return "Something went wrong.";
  if (error.kind === "network") return "Could not reach the historical statistics service. Check your connection.";
  if (error.kind === "http") {
    const status = error.status ? ` (HTTP ${error.status})` : "";
    return `The historical statistics service returned an error${status}.`;
  }
  if (error.kind === "schema") return "The historical statistics data is in an unexpected format.";
  return "Something went wrong loading historical statistics data.";
}

function staleMessage(latestTimestampIso) {
  const relative = formatRelativeTime(latestTimestampIso);
  return relative
    ? `Data may be stale -- most recent snapshot ${relative}.`
    : "Data may be stale.";
}

// staleAfterMs (if supplied) is compared against the most recent
// snapshot's own timestamp -- there is no metadata.generated_at on
// this payload for core/api.js's built-in staleness mechanism to read,
// so it is deliberately not used here (see the module comment).
export function deriveHistoryState({ payload, error = null, staleAfterMs } = {}) {
  if (!payload) {
    return { status: "error", data: null, error, isStale: false };
  }
  const data = transformHistoryData(payload);
  const status = data.snapshots.length === 0 ? "empty" : "success";

  let isStale = false;
  if (status === "success" && Number.isFinite(staleAfterMs)) {
    const latest = data.snapshots[data.snapshots.length - 1];
    isStale = getStaleness(latest.timestamp, new Date(), staleAfterMs).isStale;
  }

  return { status, data, error, isStale };
}

